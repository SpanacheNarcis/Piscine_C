	if (argc != 2)
		write(1, "a", 1);
	else
	{
		while (*argv[1])
		{
				if (*argv[1] == 'a')
				{
					write(1, "a", 1);
					argv[1]++;
					break ;
				}
				argv[1]++;
		}
	}
