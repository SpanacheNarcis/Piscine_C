#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int fr[128];

	if (ac == 3)
	{
		i = 0;
		while (i < 128)
			fr[i++] = 0;
		i = 0;
		while (av[1][i])
		{
			if (fr[av[1][i]] == 0)
			{
				fr[av[1][i]] = 1;
				write(1, &av[1][i], 1);
			}
			i++;
		}
		i = 0;
		while (av[2][i])
		{
			if(fr[av[2][i]] == 0)
			{
				fr[av[2][i]] = 1;
				write(1, &av[2][i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
