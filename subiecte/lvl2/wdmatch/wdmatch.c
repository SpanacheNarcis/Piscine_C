#include <unistd.h>

int		main(int ac, char **av)
{
	int i = 0;
	int j;
	int len = 0;

	if (ac == 3)
	{
		while (av[1][i])
		{
			i++;
			len ++;
		}

		j = 0;
		i = 0;
		while (av[2][j] && i < len)
		{
			if (av[2][j++] == av[1][i])
				i++;
		}
		if (i == len)
			write(1, av[1], len);
	}
	write(1, "\n", 1);
	return (0);
}
