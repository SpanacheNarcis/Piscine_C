#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		n;
	char	*dest;
	char	*aux;

	aux = src;
	n = 0;
	while (*aux)
	{
		n++;
		aux++;
	}
	dest = (char *)malloc(n * sizeof(char));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
