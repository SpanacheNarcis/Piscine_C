#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_generic(void)
{
	char *str;

	str = "Tut tut ; Tut tut";
	ft_putstr(str);
	ft_putchar('\n');
}

int		main(void)
{
	ft_generic();
	return (0);
}
