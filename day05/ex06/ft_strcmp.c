int		ft_strcmp(char *s1, char *s2)
{
	int		i;
	char	*aux;
	char	*aux1;

	i = 0;
	aux = s1;
	aux1 = s2;
	while (*aux != '\0' || *aux1 != '\0')
	{
		if (*aux < *aux1)
			return (-1);
		if (*aux > *aux1)
			return (1);
		if (*aux == *aux1)
		{
			aux++;
			aux1++;
		}
	}
	return (0);
}
