int		ft_atoi(char *str)
{
	int		nr;
	char	*aux;
	int		sign;

	aux = str;
	nr = 0;
	sign = 1;
	while (*aux == ' ' || (*aux >= 9 && *aux <= 13))
		aux++;
	if (*aux == '-')
		sign = -1;
	if (*aux == '-' || *aux == '+')
		aux++;
	while ('0' <= *aux && *aux <= '9')
	{
		nr = nr * 10 + sign * (*aux - '0');
		aux++;
	}
	return (nr);
}
